package org.jinghouyu.doc.rtf.protocol2;

public class UC {

	private int depth;
	private int uc;
	
	public UC(int depth, int uc) {
		this.depth = depth;
		this.uc = uc;
	}
	
	public int getDepth() {
		return depth;
	}
	public void setDepth(int depth) {
		this.depth = depth;
	}
	public int getUc() {
		return uc;
	}
	public void setUc(int uc) {
		this.uc = uc;
	}
	
	
}
