	private static Map<String, ControlName> specials = new HashMap<String, ControlName>();
	
	static {
		specials.put("*", asterisk);
		specials.put("~", tilde);
		specials.put("-", middleline);
		specials.put("_", underline);
		specials.put(":", colon);
		specials.put("'", singlequote);
		specials.put("\\", backslash);
		specials.put("{", leftbrace);
		specials.put("}", rightbrace);
		specials.put("|", or);
		specials.put("do", _do);
		specials.put("private", _private);
		specials.put("super", _super);
	}
	
	public static ControlWord valueOf1(String name) {
		ControlName controlName = specials.get(name);
		if(controlName != null) {
			return new ControlWord(controlName);
		}
		controlName = valueOf(name);
		if(controlName != null) {
			return new ControlWord(controlName);
		}
		return new ControlWord(name);
	}
	
